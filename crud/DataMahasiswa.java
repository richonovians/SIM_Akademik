    private void tabelMhsMouseClicked(java.awt.event.MouseEvent evt) {                                      
        try {
            int row = MhsTabel.getSelectedRow();
            String tableClick = (MhsTabel.getModel().getValueAt(row, 0).toString());
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery("SELECT * FROM mahasiswa WHERE Nim ='" + tableClick + "'");
            if (rs.next()) {
                String nim = rs.getString("Nim");
                nimField.setText(nim);
                String nama = rs.getString("Nama_Mhs");
                namaMhsField.setText(nama);
                String tglLahir = rs.getString("Tgl_Lahir");
                tglLahirField.setText(tglLahir);
                String alamat = rs.getString("Alamat");
                alamatField.setText(alamat);
                String jenisKelamin = rs.getString("Jenis_Kelamin");
                if (jenisKelamin.equals("Laki-laki")) {
                    JKRadioBtn1.setSelected(true);
                } else {
                    JKRadioBtn2.setSelected(true);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

