    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        String nim = nimField.getText();
        String nama = namaMhsField.getText();
        String tglLahir = tglLahirField.getText();
        String alamat = alamatField.getText();
        String jenisKelamin = JKRadioBtn1.isSelected() ? "Laki-laki" : "Perempuan";
        
        if (nim.isEmpty() || nama.isEmpty() || tglLahir.isEmpty() || alamat.isEmpty() || (!JKRadioBtn1.isSelected() && !JKRadioBtn2.isSelected())) {
            JOptionPane.showMessageDialog(null, "Pilih data mahasiswa yang ingin diubah.");
            return;
        }

        String sql = "UPDATE mahasiswa SET Nama_Mhs = ?, Tgl_Lahir = ?, Alamat = ?, Jenis_Kelamin = ? WHERE Nim = ?";
        try (PreparedStatement pst = con.prepareStatement(sql)) {
            pst.setString(1, nama);
            pst.setString(2, tglLahir);
            pst.setString(3, alamat);
            pst.setString(4, jenisKelamin);
            pst.setString(5, nim);
            int updated = pst.executeUpdate();
            if (updated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diperbarui");
                display(); // Refresh table view
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal diperbarui");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        clearData();
    }                                       

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {                                          
        // TODO add your handling code here:
        String nim = nimField.getText(); // Get the NIM from the text field

        if (nim.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data mahasiswa yang ingin dihapus.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Penghapusan", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM mahasiswa WHERE Nim = ?";
            try (PreparedStatement pst = con.prepareStatement(sql)) {
                pst.setString(1, nim);
                int deleted = pst.executeUpdate();
                if (deleted > 0) {
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    display(); // Refresh table view
                } else {
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

        clearData();
    }                                         

